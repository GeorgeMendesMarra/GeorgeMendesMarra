nome = "mundo"
# print(): Esta é uma função embutida em Python que exibe a saída no console.
# f"Olá, {nome}!": Esta é uma string formatada (f-string). 
# F-strings permitem incorporar expressões Python dentro de strings, colocando um f antes das aspas. 
print(f"Olá, {nome}!")
# O código cria uma variável chamada nome com o valor "mundo" e, em seguida, 
# usa uma f-string para imprimir uma saudação personalizada que inclui o valor da variável.
