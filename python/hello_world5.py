nome = "mundo"
print("Olá, {}!".format(nome))

# print(): Esta é uma função embutida em Python que exibe a saída no console.
# "Olá, {}!".format(nome): Esta é uma string formatada usando o método .format().
# "Olá, {}!": Esta é a string principal, com um marcador {} que será substituído pelo valor da variável nome.
#.format(nome): Este é o método .format() que substitui o marcador {} pelo valor da variável nome.
