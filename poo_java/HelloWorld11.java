/*
 * Versão Java: 8+
 * Paradigma: Imperativo
 */
public class HelloWorld11 { // Classe principal
    public static void main(String[] args) { // Método principal
        String mensagem = "Hello World"; // Define a mensagem
        int contador = 0; // Inicializa o contador
        while (contador < 1) { // Enquanto contador for menor que 1
            System.out.println(mensagem); // Imprime a mensagem
            contador++; // Incrementa o contador
        } // Fim do while
    } // Fim do main
} // Fim da classe
