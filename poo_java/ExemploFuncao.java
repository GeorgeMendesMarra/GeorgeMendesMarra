public class ExemploFuncao {

    // Função que imprime a primeira parte de uma mensagem especial.
    public static void mostrarMensagem1() {
        System.out.println("Sou uma mensagem muito especial: parte 1");
    }

    // Função que imprime a segunda parte de uma mensagem especial.
    public static void mostrarMensagem2() {
        System.out.println("Sou uma mensagem muito especial: parte 2");
    }

    // Função principal que inicia a execução do programa.
    public static void main(String[] args) {
        System.out.println(); // Imprime uma linha em branco para melhor formatação.

        mostrarMensagem1(); // Chama a função mostrarMensagem1 para imprimir a primeira parte da mensagem.

        System.out.println(); // Imprime uma linha em branco para melhor formatação.

        mostrarMensagem2(); // Chama a função mostrarMensagem2 para imprimir a segunda parte da mensagem.

        System.out.println(); // Imprime uma linha em branco para melhor formatação.
    }
}
