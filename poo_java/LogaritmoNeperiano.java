public class LogaritmoNeperiano {
    public static void main(String[] args) {
        // ------------------------
        // CÁLCULO DO COMPRIMENTO DE UM CÍRCULO
        // Fórmula matemática: C = 2 * π * r
        // Onde:
        // - C é o comprimento da circunferência
        // - π (Math.PI) é a constante Pi (aproximadamente 3.14159)
        // - r é o raio da circunferência
        // ------------------------
        
        float raio = 3; // Define o raio do círculo como 3 unidades
        double comp = 2 * raio * Math.PI; // Calcula o comprimento usando a fórmula
        System.out.println(comp); // Exibe o valor do comprimento

        // ------------------------
        // LOGARITMO NEPERIANO / NÚMERO DE EULER (e)
        // O número 'e' é uma constante irracional ≈ 2.71828
        // Ele é a base dos logaritmos naturais (ln), ou seja, logaritmo de base 'e'
        // Em Java, Math.E representa essa constante
        // ------------------------

        System.out.println(Math.E); // Exibe o valor da constante e
    }
}

        
