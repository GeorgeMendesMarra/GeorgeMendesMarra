// Este é um programa básico em C que exibe "Hello World" no console.
// O C usa comentários de linha única (//) ou de múltiplas linhas (/* */).

// 1. Inclui a biblioteca de entrada e saída padrão (Standard Input/Output).
// Esta biblioteca é necessária para usar a função 'printf()'.
#include <stdio.h>

// 2. Define a função principal (main function).
// 'int' indica que a função retornará um valor inteiro.
int main() {
    // 3. Imprime a mensagem "Hello World!" no console.
    // A função 'printf()' é a função padrão em C para formatar e imprimir saídas.
    // '\n' é um caractere de nova linha (quebra de linha).
    printf("Hello World!\n");

    // 4. Retorna 0 (zero).
    // O valor 0 indica que o programa foi executado com sucesso e encerrou normalmente.
    return 0;
}
