! Este é um programa básico em Fortran que exibe "Hello World".
! Em Fortran, o ponto de exclamação (!) marca o início de um comentário de linha única.

program hello_world 
! A instrução 'program' define o início e o nome do programa principal.

    ! A instrução 'print' é o modo mais simples de saída em Fortran moderno.
    ! O asterisco (*) em 'print *' indica que o compilador deve usar a formatação padrão.
    print *, 'Hello World!'

end program hello_world
! A instrução 'end program' marca o fim do programa, e o nome é opcional, mas recomendado.
