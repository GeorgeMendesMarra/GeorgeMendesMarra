#include <iostream> // Esta linha inclui a biblioteca padrão de entrada e saída,
                    // que permite exibir texto na tela.

int main() { // Esta é a função principal onde a execução do programa começa.
             // Todo programa em C++ deve ter uma função 'main'.

    /*
     * O 'std::cout' representa a saída padrão (geralmente o monitor).
     * O operador '<<' é usado para "inserir" o texto na saída.
     * "Hello, World!" é a mensagem de texto que queremos exibir.
     * 'std::endl' insere uma nova linha após a mensagem.
     */
    std::cout << "Hello, World!" << std::endl;

    return 0; // O 'return 0;' indica que o programa foi executado com sucesso.
}
